# se você estiver em um computador antigo será necessário utilizar o \n e \r= CRLF para pular a linha.
# o "sep" = ao printar as informações, ele utilizar o argumento mencionado.
# end significa como você gostaria de costumizar o fim do print.

print(12, 33, 66, sep=" - ", end= '' )
print(55, 44, 22, sep=" - " )
print(159, 63, 77, sep=" - " )

#para destacar um texto, você poderá utilizar: \" ou essa sequência abaixo.
